<h1>3. WAP to show the use of switch</h1>
<?php
$day="sun";
switch ($day){
    case"mon":
        echo"today is monday.";
        break;
    case"tus":
        echo"today is tuseday.";
        break;
    case"sun":
        echo"today is sunday.";
        break;
    default:
         echo"try again.";
            
}
?>
